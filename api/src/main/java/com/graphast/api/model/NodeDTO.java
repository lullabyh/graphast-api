package com.graphast.api.model;

public class NodeDTO {
	
	private long id;
	
	private long lat;
	
	private long lng;

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public long getLat() {
		return lat;
	}

	public void setLat(long lat) {
		this.lat = lat;
	}

	public long getLng() {
		return lng;
	}

	public void setLng(long lng) {
		this.lng = lng;
	}
	
	
	

}
